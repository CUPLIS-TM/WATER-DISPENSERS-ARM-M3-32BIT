/**
  * @brief  System startup sequence
  * @param  None
  * @retval None
  */
static void System_Startup(void)
{
  // Initialize outputs to safe state
  PUMP_OFF();
  PROGRAM_LED_OFF();
  STATUS_LED_OFF();

  // Power-on stabilization delay
  HAL_Delay(500);

  // Startup blink sequence (3x fast blink = system starting)
  for(int i = 0; i < 3; i++) {
    PROGRAM_LED_ON();
    STATUS_LED_ON();
    HAL_Delay(150);
    PROGRAM_LED_OFF();
    STATUS_LED_OFF();
    HAL_Delay(150);
  }

  HAL_Delay(500);
}
